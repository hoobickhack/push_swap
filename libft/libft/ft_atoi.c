#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			sign;
	long int	result;

	i = 0;
	sign = 1;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - 48;
		i++;
	}
	if ((result * sign) > INT_MAX || (result * sign) < INT_MIN)
		writeerreur1(1);
	return (result * sign);
}

void	writeerreur1(int a)
{
	if (a == 1)
		write (1, "Error\n check INT_MAX or INT_MIN", 31);
	exit(EXIT_FAILURE);
}
